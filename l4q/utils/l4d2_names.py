def get_campaign_name(campaign_display_name: str) -> str:
    """Get the name of a campaign.
    
    :param str campaign_display_name: Campaign display name.
    :return: The name of the campaign.
    :rtype: str
    """
    campaign_name = CAMPAIGN_NAMES.get(campaign_display_name)
    if campaign_name is not None:
        return campaign_name
    return campaign_display_name

def get_chapter_name(chapter_disp_name: str,
                     chapter: int,
                     game_mode: str,
                     maxplayers: int = 4) -> str:
    """Get the name of the chapter.
    
    :param str chapter_disp_name: Chapter display string.
    :param int chapter: The current chapter.
    :param str game_mode: Game mode string.
    :param int maxplayers: Max player count, defaults to 4.
    :return: The name of the chapter, "Chapter X" if the chapter name cannot be resolved.
    :rtype: str
    """
    generic_chapter_name = f"Chapter {chapter}"
    chapter_dict: dict = CHAPTER_NAMES.get(chapter_disp_name)
    if chapter_dict is None:
        return generic_chapter_name
    if game_mode.startswith("l4d1"):
        if game_mode == "l4d1vs":
            game_mode = "versus"
        else:
            game_mode = game_mode.removeprefix("l4d1")
    # Follow the Liter mutation
    if game_mode == "mutation13":
        game_mode = "scavenge"
    # Versus Survival mode
    if game_mode == "mutation15":
        game_mode = "survival"
    game_mode_dict: dict = chapter_dict.get(game_mode)
    if game_mode_dict is None:
        if maxplayers == 4:
            game_mode_dict = chapter_dict.get("coop")
        else:
            game_mode_dict = chapter_dict.get("versus")
    chapter_str = game_mode_dict.get(chapter)
    if chapter_str is None:
        return generic_chapter_name
    return chapter_str
